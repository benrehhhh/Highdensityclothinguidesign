interface Marker {
  position: [number, number];
  icon: L.Icon;
  popupContent: string;
}

interface MapWrapperProps {
  center: [number, number];
  zoom: number;
  markers: Marker[];
  polyline?: [number, number][];
  className?: string;
}

export function MapWrapper({ center, zoom, markers, polyline, className }: MapWrapperProps) {
  const mapRef = useRef<HTMLDivElement>(null);
  const mapInstanceRef = useRef<L.Map | null>(null);

  useEffect(() => {
    if (!mapRef.current) return;

    // Initialize map
    if (!mapInstanceRef.current) {
      const map = L.map(mapRef.current).setView(center, zoom);

      L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '&copy; OpenStreetMap contributors'
      }).addTo(map);

      mapInstanceRef.current = map;
    }

    const map = mapInstanceRef.current;

    // Clear existing markers and polylines
    map.eachLayer((layer) => {
      if (layer instanceof L.Marker || layer instanceof L.Polyline) {
        map.removeLayer(layer);
      }
    });

    // Add markers
    markers.forEach(({ position, icon, popupContent }) => {
      L.marker(position, { icon })
        .addTo(map)
        .bindPopup(popupContent);
    });

    // Add polyline if provided
    if (polyline && polyline.length > 0) {
      L.polyline(polyline, { color: 'blue', weight: 3, opacity: 0.6 }).addTo(map);
    }

    // Update center
    map.setView(center, zoom);

    // Cleanup
    return () => {
      if (mapInstanceRef.current) {
        mapInstanceRef.current.remove();
        mapInstanceRef.current = null;
      }
    };
  }, [center, zoom, markers, polyline]);

  return <div ref={mapRef} className={className} style={{ height: '100%', width: '100%' }} />;
}
